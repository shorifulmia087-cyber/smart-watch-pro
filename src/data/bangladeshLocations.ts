// Flatten to a searchable list of upazilas with parent mapping
let _cache: Upazila[] | null = null;

export const getAllUpazilas = (): Upazila[] => {
  if (_cache) return _cache;
  const result: Upazila[] = [];
  for (const div of BD_DATA) {
    for (const dist of div.districts) {
      for (const uz of dist.upazilas) {
        result.push({ name: uz, district: dist.district, division: div.division });
      }
    }
  }
  _cache = result;
  return result;
};

// Get unique divisions
export const getDivisions = (): string[] => BD_DATA.map(d => d.division);

// Get districts for a division
export const getDistricts = (division: string): string[] => {
  const div = BD_DATA.find(d => d.division === division);
  return div ? div.districts.map(d => d.district) : [];
};
